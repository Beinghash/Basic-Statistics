"""Observations:


*   Histogram is not well distributed, it is skewed a little towards the right
*   As we have seen skeweness, therefore we can check the distribution usin distplot function

**Skewness** is a statistical measure that describes the asymmetry of a data distribution. It tells us whether the data is symmetrically distributed or leans more toward one side.
 Types of Skewness:
1. **Positive Skewness (Right-Skewed)**
  - The tail on the right side is longer.

  - Mean > Median > Mode

  - Most of the data values are on the lower end, but a few large values pull the mean to the right.

ðŸ“ˆ Example: Income distribution â€” many people earn average or low incomes, but a few earn very high incomes.

2. **Negative Skewness (Left-Skewed)**
  -  The tail on the left side is longer.
  - Mean < Median < Mode

  - Most of the data values are on the higher end, but a few very low values pull the mean to the left.

ðŸ“‰ Example: Age at retirement â€” most people retire around the same age, but a few retire very early.

3. **Zero Skewness (Symmetrical Distribution)**
 - The left and right sides of the distribution are mirror images.

 - Mean = Median = Mode

 - This is typical in a normal distribution (bell-shaped curve).




"""

